% 定义初始条件函数
ic = @(x) sin(pi*x); 

% 调用 heat1d_explicit 函数求解一维热传导方程
% 参数含义：
% 0.1: 空间步长
% 1: 空间区域长度
% 0.5: 时间步长
% 50: 空间网格点数
% 200: 时间网格点数
% ic: 初始条件函数句柄
% 0: 左边界条件
% 0: 右边界条件
[x,t,U] = heat1d_cn(0.1,1,0.5,50,200,ic,0, 0);

% 使用 imagesc 函数绘制二维图像，展示温度分布随时间和空间的变化
imagesc(x,t,U); 

% 添加颜色条，用于表示温度值
colorbar; 

% 添加 x 轴标签
xlabel('x'); 

% 添加 y 轴标签
ylabel('t'); 

% 添加图像标题
title('1D 热方程 显式');